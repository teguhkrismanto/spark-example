package spark;

public class Model {

	public String user_name;
	public String no_ponsel;
	public String jumlah_anggota_keluarga;
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getNo_ponsel() {
		return no_ponsel;
	}
	public void setNo_ponsel(String no_ponsel) {
		this.no_ponsel = no_ponsel;
	}
	public String getJumlah_anggota_keluarga() {
		return jumlah_anggota_keluarga;
	}
	public void setJumlah_anggota_keluarga(String jumlah_anggota_keluarga) {
		this.jumlah_anggota_keluarga = jumlah_anggota_keluarga;
	}

	
}
